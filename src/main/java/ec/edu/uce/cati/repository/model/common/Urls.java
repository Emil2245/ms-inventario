package ec.edu.uce.cati.repository.model.common;

public class Urls {

    /**
     * URL a la imagen en su resolución y calidad original.
     */
    public String original;

    /**
     * URL a una versión de vista previa, de tamaño mediano (e.g., para listados).
     */
    public String preview;

    /**
     * URL a una versión de baja resolución (e.g., thumbnails, íconos).
     */
    public String lowres;
}